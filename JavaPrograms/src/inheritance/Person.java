package inheritance;

public class Person {
   void Input() {
	   System.out.println("Enter your Name");
   }
}
 class Ankit extends Person {
	 void show() {
		 System.out.println("My Name is Ankit");
	 }
 }
 class Rahul extends Person {
	 void Display() {
		 System.out.println("My name is Rahul");
	 }
 }